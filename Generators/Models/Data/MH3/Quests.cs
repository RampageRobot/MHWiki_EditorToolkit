		public static Quests[] Fetch(string pathToFolders)
		{
			return FromDir(pathToFolders);
		}

		public static Quests[] FromDir(string dir)
		{
			List<Quests> ret = [];
			foreach (string childDir in Directory.GetDirectories(dir))
			{
				ret.AddRange(FromDir(childDir));
			}
			foreach (string file in Directory.GetFiles(dir))
			{
				ret.Add(FromFile(file));
			}
			return [.. ret];
		}

		public static Quests FromFile(string file)
		{
			return JsonConvert.DeserializeObject<Quests>(File.ReadAllText(file))!;
		}
	}

